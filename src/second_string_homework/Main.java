package second_string_homework;

public class Main {

    public static void main(String[] args) {
        SecondStringHomework str = new SecondStringHomework();

        System.out.println(str.firstSymbols("ozymandias"));

        System.out.println(str.countXX("dxxx"));

        System.out.println(str.palindrome("abcba"));

        System.out.println(str.toLoweCaseA(new String[]{"asd", "adsfsd", "bas"}));

        System.out.println(str.toLoweCaseA(new String[]{"aaa", "bbb", "acs"}));

        System.out.println(str.commonCharacterCount("Jv", "caeJvmic"));

        System.out.println(str.longestCommonSubstring("java", "javlkll"));

    }
}
